public class Statemen_While {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // Perulangan While
        int i=0;
        
        while (i<=10){
          // blok kode yang akan diulang
            System.out.println("Perulangan ke-" +i);
            //increment nilai i
            i++;
        }
    }
    
}
